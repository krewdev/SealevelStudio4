"use client";

import React, { useState, useEffect } from "react";
import { X, Zap } from "lucide-react";

interface UIComponent {
  id: string;
  type: string;
  config: any;
  timestamp: number;
}

interface GenerativeUIProps {
  components: UIComponent[];
  onClose: (id: string) => void;
}

export function GenerativeUI({ components, onClose }: GenerativeUIProps) {
  return (
    <>
      {components.map((component) => (
        <GeneratedComponent
          key={component.id}
          component={component}
          onClose={() => onClose(component.id)}
        />
      ))}
    </>
  );
}

function GeneratedComponent({ component, onClose }: { component: UIComponent; onClose: () => void }) {
  const [isVisible, setIsVisible] = useState(false);

  useEffect(() => {
    // Animate in
    setTimeout(() => setIsVisible(true), 10);
  }, []);

  const renderComponent = () => {
    switch (component.type) {
      case "swap_card":
        return (
          <SwapCard
            tokens={component.config.tokens || ["SOL", "USDC"]}
            amount={component.config.amount || 0}
          />
        );
      case "dashboard":
        // Old dashboard component removed - no longer supported
        return null;
      case "wallet_balance":
        return <WalletBalanceCard />;
      case "transaction_builder":
        return <TransactionBuilderCard config={component.config} />;
      default:
        return (
          <div className="p-6 bg-black/80 backdrop-blur-2xl border border-white/20 rounded-2xl">
            <div className="text-white">Unknown component type: {component.type}</div>
            <pre className="text-xs text-gray-400 mt-2">{JSON.stringify(component.config, null, 2)}</pre>
          </div>
        );
    }
  };

  return (
    <div
      className={`fixed top-1/2 left-1/2 -translate-x-1/2 -translate-y-1/2 z-30 transition-all duration-500 ${
        isVisible ? "opacity-100 scale-100" : "opacity-0 scale-95"
      }`}
    >
      <div className="relative">
        {renderComponent()}
        <button
          onClick={onClose}
          className="absolute -top-2 -right-2 p-1.5 bg-gray-800 hover:bg-gray-700 rounded-full border border-gray-600 transition-colors"
        >
          <X className="w-4 h-4 text-gray-400" />
        </button>
      </div>
    </div>
  );
}

